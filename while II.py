# Solicita ao usuário o valor desejado para iniciar a contagem
inicio = int(input('Qual valor deseja iniciar a contagem? '))

# Solicita ao usuário o valor desejado para encerrar a contagem
final = int(input('Qual valor deseja encerrar a contagem? '))

# Executa um loop enquanto o valor de "inicio" for menor ou igual ao valor final
while inicio <= final:
    # Incrementa o valor de "inicio" em 1, adicionando 1 à variável atual
       inicio = inicio + 1
    # Imprime o valor atual de "inicio"
       print(inicio)






